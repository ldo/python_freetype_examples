class CodeBlock :
    "describes a Unicode code block by its name and the bounds of its code range."

    def __init__(self, name, lo, hi) :
        self.name = name
        self.lo = lo
        self.end = hi + 1
    #end __init__

    @property
    def range(self) :
        "returns a range object covering the code range."
        return \
            range(self.lo, self.end)
    #end range

#end CodeBlock

